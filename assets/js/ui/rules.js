// Экран «Правила»: кратко и понятно про подсчёт очков.
import { h } from './components.js';

export function renderRules(view, ctx) {
  const c = ctx.S.app.scoring;

  view.append(h('h1', { class: 'view-title' }, [h('span', { class: 'accent', text: 'Правила' }), ' турнира']));

  // Как играть
  view.append(
    h('div', { class: 'rules' }, [
      h('div', { class: 'card' }, [
        h('h2', {}, ['🎯 Как играть']),
        h('p', { text: 'На каждый матч ты ставишь точный счёт и до трёх авторов голов. Ставку (и счёт, и игроков) можно менять сколько угодно — но только до стартового свистка. После начала матча ставка блокируется, и её видят все участники.' }),
        h('p', {}, ['Ставки открываются ', h('b', { text: 'по турам' }), ': матчи 2-го тура группы становятся доступны после завершения 1-го, 3-го — после 2-го; плей-офф — после предыдущей стадии. Доступные матчи можно заполнить сразу все.']),
        h('p', { text: 'Список заявленных игроков обновляется в день матча (травмы, изменения состава).' }),
        h('p', {}, ['Перед турниром (до матча открытия) выбери ', h('b', { text: 'чемпиона мира' }), ' и ', h('b', { text: 'лучшего бомбардира' }), ' — за них дают крупные бонусы в самом конце.']),
      ]),

      // Очки за счёт
      h('div', { class: 'card' }, [
        h('h2', {}, ['📊 Очки за счёт ', h('span', { class: 'pill x16', text: 'макс ' + c.scoreBlockMax })]),
        table([
          ['Точный счёт', `${c.exact}`, 'Джекпот — угадал «как в табло»'],
          ['Исход + разница мячей', `${c.outcome + c.goalDiff}`, 'Напр. поставил 2:1, вышло 3:2'],
          ['Только исход (П1 / Х / П2)', `${c.outcome}`, 'Угадал победителя или ничью'],
          ['Голы одной команды точно', `+${c.teamGoals}`, 'За каждую команду отдельно (до +' + c.teamGoals * 2 + ')'],
          ['Суммарное число голов', `+${c.totalGoals}`, 'Всего голов в матче совпало'],
        ]),
        h('p', { class: 'potential', text: 'Бонусы за голы команд и за сумму голов суммируются с очками за исход, но итог по счёту не больше ' + c.scoreBlockMax + '. Точный счёт сразу даёт ' + c.exact + '.' }),
        h('p', { class: 'potential' }, ['⏱ ', h('b', { text: 'Плей-офф:' }), ' ставка проверяется и по счёту основного времени, и по итогу с дополнительным — засчитывается лучший. Если матч шёл 1:1 и закончился 2:1 в доп. время, точный счёт (', h('b', { text: '' + c.exact }), ') получат оба: и кто поставил 1:1, и кто поставил 2:1.']),
      ]),

      // Авторы
      h('div', { class: 'card' }, [
        h('h2', {}, ['👟 Очки за авторов голов']),
        table([
          ['Каждый угаданный автор', `+${c.scorerEach}`, 'Выбираешь 3 игрока, до +' + c.scorerEach * c.scorersPerBet],
          ['Хет-трик прогноза', `+${c.hatTrick}`, 'Все 3 автора угаданы, и в матче 3+ гола'],
        ]),
        h('p', { class: 'potential', text: 'Автор засчитан, если названный игрок забил хотя бы один гол (автоголы не считаются). Порядок не важен.' }),
      ]),

      // Множители
      h('div', { class: 'card' }, [
        h('h2', {}, ['🔥 Коэффициент сложности']),
        h('p', { text: 'Сумма очков за матч умножается на коэффициент — он назначается автоматически по стадии и рейтингу ФИФА.' }),
        h('div', { class: 'pillrow' }, [
          h('span', { class: 'pill x1', text: '×1.0 — явный фаворит (группа)' }),
          h('span', { class: 'pill x13', text: '×1.3 — равные (группа)' }),
          h('span', { class: 'pill x16', text: '×1.6 — топ-матч / 1/8' }),
          h('span', { class: 'pill x20', text: '×2.0 — с 1/4 и дальше' }),
        ]),
        h('ul', {}, [
          h('li', { text: '×1.0 — групповой этап, разница в рейтинге ФИФА больше 30 мест' }),
          h('li', { text: '×1.3 — групповой этап, примерно равные команды' }),
          h('li', { text: '×1.6 — топ-матч группы (обе в топ-12 ФИФА) или 1/8 финала' }),
          h('li', { text: '×2.0 — 1/4 финала, 1/2 финала и финал' }),
        ]),
        h('p', { class: 'potential', text: 'Итог за матч = (очки за счёт + очки за авторов) × коэффициент, округляем до целого (54.6 → 55).' }),
      ]),

      // Рейтинг ФИФА
      rankingCard(ctx),

      // Бонусы
      h('div', { class: 'card' }, [
        h('h2', {}, ['🎁 Бонусы']),
        table([
          ['Точный счёт в особом матче', `+${c.exactSpecialBonus}`, 'Открытие, 1/8, 1/4, 1/2, финал'],
          ['Джекпот тура', `+${c.roundJackpot}`, 'Тому, кто набрал больше всех очков в туре'],
          ['Угадал чемпиона мира', `+${c.championBonus}`, 'Начисляется в конце турнира'],
          ['Угадал лучшего бомбардира', `+${c.topScorerBonus}`, 'Начисляется в конце турнира'],
        ]),
        h('p', { class: 'potential', text: 'Туры: каждый из трёх групповых туров, 1/8, 1/4, 1/2, финал. При равенстве лидеров тура джекпот получает каждый.' }),
      ]),

      // Пример
      h('div', { class: 'card' }, [
        h('h2', {}, ['🧮 Пример']),
        h('p', {}, ['Ставка ', h('b', { text: '2:1' }), ', забьют игроки A, B, C. Матч 1/4 финала (×2.0).']),
        h('p', { text: 'Вышло 2:1, забили A и B (2 гола). Очки за счёт: точный — 20. Авторы: 2 угаданных × ' + c.scorerEach + ' = ' + c.scorerEach * 2 + '. База ' + (20 + c.scorerEach * 2) + ' × 2.0 = ' + (20 + c.scorerEach * 2) * 2 + ', плюс ' + c.exactSpecialBonus + ' за точный счёт в 1/4 = ' + ((20 + c.scorerEach * 2) * 2 + c.exactSpecialBonus) + ' очков.' }),
      ]),

      h('div', { class: 'card' }, [
        h('h2', {}, ['🔒 Честность']),
        h('p', { text: 'Чужие ставки скрыты до стартового свистка — никто не подсмотрит. После начала матча они открываются всем участникам. Все данные в репозитории зашифрованы.' }),
      ]),
    ])
  );
}

function rankingCard(ctx) {
  const teams = ctx.S.fifa?.teams || {};
  const entries = Object.entries(teams).sort((a, b) => a[1] - b[1]);
  return h('div', { class: 'card' }, [
    h('h2', {}, ['🌍 Рейтинг ФИФА команд']),
    h('p', { class: 'potential', text: 'По нему определяется коэффициент сложности матча. Места приблизительные — организатор может скорректировать.' }),
    h('div', { class: 'rank-grid' }, entries.map(([name, rank]) =>
      h('div', { class: 'rank-item' }, [h('span', { class: 'n', text: '#' + rank }), h('span', { class: 't', text: name })])
    )),
  ]);
}

function table(rows) {
  return h('table', {}, [
    h('thead', {}, [h('tr', {}, [h('th', { text: 'Условие' }), h('th', { text: 'Очки' }), h('th', { text: 'Комментарий' })])]),
    h('tbody', {}, rows.map((r) => h('tr', {}, [h('td', { text: r[0] }), h('td', { class: 'n', text: r[1] }), h('td', { text: r[2] })]))),
  ]);
}
